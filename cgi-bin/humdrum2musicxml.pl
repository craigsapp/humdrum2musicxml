use strict;
use CGI;
use Digest::MD5 qw(md5 md5_hex md5_base64);
use IPC::Open2; 

my $cgi_form = new CGI;

# Configuration variables:
my $basedir      = "/project/humdrum2musicxml";
my $cachedir     = "$basedir/cache";
my $maxsize      = 1000000;  # largest file size to allow (1MB)

# Command-line tools:
my $humdrum2musicxml = "/usr/bin/python3 -m converter21 -f humdrum -t musicxml - -";
my $cleanMusicxml    = "$basedir/bin/cleanMusicxml";

##########################################################################
# Main Input Extraction & Processing Flow
##########################################################################

my $File = $cgi_form->param('inputdata');
my $inputdata = "";

if (defined $File) {
	# Safe check to see if Apache passed an open stream/filehandle reference
	if (ref($File) || fileno($File)) {
		my $Buffer = "";
		while (read($File, $Buffer, 1024)) {
			$inputdata .= $Buffer;
		}
	} else {
		# If it's a plain text string (from LWP/curl), assign it directly
		$inputdata = $File;
	}
}

# Validate that the score payload isn't empty
if ($inputdata =~ /^\s*$/) {
	errorMessage("Error: no input score");
} else {
	convertToMusicxml($inputdata);
}

exit(0);

##########################################################################
# Core Conversion Logic
##########################################################################

sub convertToMusicxml {
	my ($inputdata) = @_;

	# Enforce file size checks based on the HTTP transaction context
	if (defined $ENV{'CONTENT_LENGTH'} && $ENV{'CONTENT_LENGTH'} > $maxsize) {
		errorMessage("Input data is too large");
		return;
	}

	# Define the system shell tool chain
	my $command = "$humdrum2musicxml | $cleanMusicxml";
#	my $command = "$humdrum2musicxml ";
	print STDERR "COMMAND RUNNING: $command\n";

	my $outputdata = "";

	# Open bidirectional filehandles ($reader reads output, $writer sends input)
	eval {
		my ($reader, $writer);
		my $pid = open2($reader, $writer, $command);

		# 1. Pump the Humdrum raw text into the commands standard input
		print $writer $inputdata;
		close($writer); # Close writer so the shell knows input is finished

		# 2. Extract the converted MusicXML text back from the stdout stream
		while (my $line = <$reader>) {
			$outputdata .= $line;
		}
		close($reader);

		# 3. Clean up the system process record
		waitpid($pid, 0);
	};

	# Trap execution exceptions (e.g. if converter21 or python environment is missing)
	if ($@) {
		errorMessage("Backend pipeline execution failed: $@");
		exit(0);
	}

	print STDERR "OUTPUT SIZE: " . length($outputdata) . " bytes\n";

	# Verify if the returned data contains a valid XML declaration header
	if ($outputdata =~ /^<\?xml/ || $outputdata =~ /^<\?mxl/) {
		print "Content-Type: text/x-musicxml\n\n";
		print $outputdata;
	} else {
		# Return execution errors generated by Python straight to the client
		errorMessage($outputdata || "Error: Conversion pipeline returned an empty response.");
	}
}

