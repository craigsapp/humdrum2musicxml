sub printParameters {
	my $output = "";
	my $name;
	my $value;
	foreach my $name ($cgi_form->param) {
		foreach my $value ($cgi_form->param($name)) {
			$output .= "$name ==\t$value\n";
		}
	}

	print "Content-Type: text/plain\n\n";
	print "CGI parameters:\n";
	print "$output\n";
}

